/**
 * 根据省份获取城市列表
 */
export function getCitiesByProvince(province: string): string[] {
  return CITIES[province] || [];
}

/**
 * 根据城市获取区县列表
 */
export function getDistrictsByCity(city: string): string[] {
  return DISTRICTS[city] || [];
}

/**
 * 检查是否为直辖市
 */
export function isMunicipality(province: string): boolean {
  return ['北京市', '天津市', '上海市', '重庆市'].includes(province);
}

/**
 * 搜索地区（支持拼音首字母）
 */
export function searchRegions(
  keyword: string,
  type: 'province' | 'city' | 'district' = 'province'
): string[] {
  if (!keyword) return [];

  const searchList =
    type === 'province'
      ? PROVINCES
      : type === 'city'
        ? Object.values(CITIES).flat()
        : Object.values(DISTRICTS).flat();

  return searchList.filter(
    item =>
      item.toLowerCase().includes(keyword.toLowerCase()) ||
      item.includes(keyword)
  );
}
